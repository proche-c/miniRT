#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*temp;

	temp = NULL;
	if (lst)
	{
		if (*lst)
		{
			temp = *lst;
			while (*lst)
			{
				*lst = (*lst)->next;
				ft_lstdelone(temp, del);
				temp = *lst;
			}
		}
		lst = NULL;
	}
}
