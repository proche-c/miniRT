#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	a;
	unsigned char	*ptr;
	size_t			i;

	a = (unsigned char)c;
	ptr = b;
	i = 0;
	while (i < len)
	{
		ptr[i] = a;
		i++;
	}
	return (b);
}
