#include "libft.h"

void	*ft_bzero(void *b, size_t len)
{
	unsigned char	*ptr;
	size_t			i;

	ptr = b;
	i = 0;
	if (len != 0)
	{
		while (i < len)
		{
			ptr[i] = 0;
			i++;
		}
	}
	return (b);
}
