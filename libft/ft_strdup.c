#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*result;
	size_t	lenth;

	lenth = ft_strlen(s1);
	result = malloc(sizeof(const char) * lenth + 1);
	if (!result)
		return (NULL);
	ft_strlcpy(result, s1, lenth + 1);
	return (result);
}
